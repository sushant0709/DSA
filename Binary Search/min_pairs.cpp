pair<int,int> min_pairs(vector<int> arr1,vector<int> arr2){

	// sort second array
	sort(arr2.begin(),arr2.end());
	int diff=INT_MAX;
	int p1;
	int p2;
	for(auto element:arr1){
		auto index=lower_bound(arr2.begin(),arr2.end(),element)-arr2.begin();

		// left/lesser (index-1)
		if(index>=1 and element-arr2[index-1]<diff){
			diff=element-arr2[index-1];
			p1=arr2[index-1];
			p2=element;
		}
		// right/greater or equal (index)
		if(index!=arr2.size() and arr2[index]-element<diff){
			diff=arr2[index]-element;
			p1=element;
			p2=arr2[index];
		}
	}
	return make_pair(p1,p2);
}
